#==============================================================================
# ** Tooltips Module
#------------------------------------------------------------------------------
#  This global module handles and shows tooltips for maximum performance.
#==============================================================================

module Tooltips
	#--------------------------------------------------------------------------
	# * Variables
	#--------------------------------------------------------------------------
	@tooltips = {}
	#--------------------------------------------------------------------------
	# * Class Declaration
	#--------------------------------------------------------------------------
	class << self
		#----------------------------------------------------------------------
		# * Accessors
		#----------------------------------------------------------------------
		attr_accessor :tooltips
		#----------------------------------------------------------------------
		# * Add Tooltip
		#----------------------------------------------------------------------
		def add(key, tooltip)
			@tooltips[key] = tooltip
		end
		#----------------------------------------------------------------------
		# * Get
		#----------------------------------------------------------------------
		def [](key) ; @tooltips[key] ; end
		#----------------------------------------------------------------------
		# * Exists?
		#----------------------------------------------------------------------
		def exists?(key)
			return @tooltips[key]
		end
		#----------------------------------------------------------------------
		# * Hide Tooltips
		#----------------------------------------------------------------------
		def hide
			@tooltips.each_value {|tooltip| tooltip.visible = false}
		end
		#----------------------------------------------------------------------
		# * Show
		#----------------------------------------------------------------------
		def show(key)
			@tooltips[key].update
			@tooltips[key].visible = true
		end
	end
end